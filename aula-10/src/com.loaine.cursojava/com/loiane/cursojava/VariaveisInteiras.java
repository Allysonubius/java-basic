package com.loiane.cursojava;

public class VariaveisInteiras {

    public static void main(String[] args){
        //Declarando variaveis
        byte idade01;
        short idade02;
        int idade03;
        long idade04;
        //Inserindo dados
        idade01 = 10;
        idade02 = 20;
        idade03 = 30;
        idade04 = 40;
        //Println
        System.out.println("Byte "+ idade01);
        System.out.println("Short "+ idade02);
        System.out.println("Int "+ idade03);
        System.out.println("Long "+ idade04);
    }
    
}

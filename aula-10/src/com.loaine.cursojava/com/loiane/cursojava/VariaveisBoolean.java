package com.loiane.cursojava;

public class VariaveisBoolean {
    
    public static void main(String[] args) {
        boolean verdadeiro;
        boolean falso;
    
        verdadeiro = true;
        falso = false;

        System.out.println("O valor é " + falso);
        System.out.println("O valor é " + verdadeiro);
    }
}
